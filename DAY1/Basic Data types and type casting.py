# BASIC DATA TYPES AND TYPE CASTING

# Strings: they are usually enclosed in parenthesis ("" or '')
'Triumph'
name = 'Triumph'
# Integers: whole numbers
5
number = 7
# Float: Decimal numbers
7.5
pi = 3.1427

print(type(number))
type(pi)
type(name)

# Type Casting: changes from one data type to another
print(int(pi))
str(number)
float(number)
