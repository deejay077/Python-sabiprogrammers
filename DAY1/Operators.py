# OPERATORS

# ARITHMETIC OPERATORS
x = 7
y = 5
z = 14
# addition (+)
2 + 2
Ans_add = x + y + z
print(Ans_add)
# subtraction (-)
6 - 3
Ans_sub = x - y - z
print(Ans_sub)
# division (/)
6 / 3
Ans = x / y
# modulus division (%) returns the division remainder
6 % 4
Ans = x % y
# floor division (//) returns the whole number part of the division 
6 // 4
Ans = x // y
# multiplication (*)
6 * 2
Ans = x * y * z
# exponential (**) 
2 ** 3
Ans = x ** y

# COMPARISON OPERATORS 
a = 12
b = 9
c = 2
# Assignment (=) used to assign values to variables
name = 'Triumph'
# Equal (==)
2 == 3
a == b
# not equal (!=)
2 != 3
a != b
# greater than or equal (>=)
2 >= 3
a >= b
# less than or equal (<=)
2 <= 3
a <= b
# greater than (>)
2 > 3
a > b > c
# less than (<)
2 < 3
a < b < c

# LOGICAL OPERATORS
num1 = 8
num2 = 2
num3 = 5
# And (&)
5 < 7 and 3 > 7
num1 > num2 and num3 < num2
(num1 > num2) & (num3 > num2)
# Or (|)
5 < 7 or 3 > 7
num1 > num2 or num3 < num2
(num1 > num2) | (num3 > num2)
# Not
not (5 < 7 or 3 > 7)
not (num1 > num2 and num3 < num2)
not ((num1 > num2) & (num3 > num2))

# IDENTITY OPERATORS
